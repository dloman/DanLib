#include "Session.hpp"

using dl::udp::Session;
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
Session::Session(const unsigned Port, const asio::ip::address_v4& IpAddress)
  : mEndpoint(IpAddress, Port),
    mData()
{
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
asio::ip::udp::endpoint& Session::GetEndpoint()
{
  return mEndpoint;
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
const asio::ip::udp::endpoint& Session::GetEndpoint() const
{
  return mEndpoint;
}
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
const char* Session::GetData() const
{
  return mData.data();
}

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
char* Session::GetData()
{
  return mData.data();
}
